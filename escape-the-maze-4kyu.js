function hasExit(maze) {
	if (maze[0].includes(' ') || maze[maze.length - 1].includes(' ')) return true;

	for (let i = 0; i < maze.length; i++) {
		if (maze[i][0] == ' ') return true;
		if (maze[i][maze[0].length - 1] == ' ') return true;
	}
	return false;
}
